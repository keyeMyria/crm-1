package com.itheima.crm.staff.domain;

import java.util.Date;

import com.itheima.crm.post.domain.CrmPost;

public class CrmStaff implements java.io.Serializable {

	private static final long serialVersionUID = 1403529946022055395L;
	private String staffId;
	private CrmPost crmPost;	//职务
	private String loginName; 	//登录名
	private String loginPwd;	//登录密码
	private String staffName;	//员工名册
	private String gender;		//性别
	private Date onDutyDate;	//入职时间
	public String getStaffId() {
		return staffId;
	}
	public void setStaffId(String staffId) {
		this.staffId = staffId;
	}
	public CrmPost getCrmPost() {
		return crmPost;
	}
	public void setCrmPost(CrmPost crmPost) {
		this.crmPost = crmPost;
	}
	public String getLoginName() {
		return loginName;
	}
	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}
	public String getLoginPwd() {
		return loginPwd;
	}
	public void setLoginPwd(String loginPwd) {
		this.loginPwd = loginPwd;
	}
	public String getStaffName() {
		return staffName;
	}
	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public Date getOnDutyDate() {
		return onDutyDate;
	}
	public void setOnDutyDate(Date onDutyDate) {
		this.onDutyDate = onDutyDate;
	}

	


}